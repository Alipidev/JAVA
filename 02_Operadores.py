#OPERADORES

#suma, resta, multiplicación y división
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)

#para saber el resto de una división
print(10 % 2)

#FLOOR DIVISION hacer una división aproximando el resultado a un numero entero
print(10 // 3)

 # ELEVANDO UN NUMERO A OTRO
print(3 ** 4)

#Operadores Comparativos, se puede hacer con cadenas de texto

print(3 > 4)
print(3 < 4)
print(3 <= 4)
print(3 >= 4)
print(3 == 4)
print(3 != 4)

#Operadores lógicos (Booleanos)

print(3 > 4 and "Hola" > "Phyton")
print(3 < 4 or "Hola" > "Phyton")
print(not(3 > 4))
